    def _dest_to_context(self, dest: str) -> str:
        """Convert destination shorthand to Asterisk context,exten,priority."""
        if not dest:
            return "app-blackhole,hangup,1"

        if dest.startswith("ivr-"):
            ivr_id = dest.split("-", 1)[1]
            return f"ivr-{ivr_id},s,1"
        if dest.startswith("ringgroup-"):
            num = dest.split("-", 1)[1]
            return f"ext-group,{num},1"
        if dest.startswith("daynight-"):
            mid = dest.split("-", 1)[1]
            return f"app-daynight,{mid},1"
        if dest.startswith("announcement-"):
            aid = dest.split("-", 1)[1]
            return f"app-announcement-{aid},s,1"
        if dest == "fax":
            return "fax-iax,s,1"
        if dest == "hangup":
            return "app-blackhole,hangup,1"

        # Assume it's already a context,exten,priority triple
        if "," in dest:
            return dest
        return f"{dest},s,1"

