    def generate_all(self) -> dict[str, str]:
        """Generate all config files, return {filename: content}."""
        return {
            "pjsip_extensions.conf": self._gen_pjsip_extensions(),
            "extensions_generated.conf": self._gen_dialplan(),
            "voicemail_generated.conf": self._gen_voicemail(),
        }

    def write_configs(self) -> list[str]:
        """Write generated configs to disk. Returns list of files written."""
        configs = self.generate_all()
        written = []
        for filename, content in configs.items():
            path = os.path.join(self.asterisk_dir, filename)
            # Atomic write
            fd, temp_path = tempfile.mkstemp(dir=self.asterisk_dir, suffix=".tmp")
            try:
                os.write(fd, content.encode())
                os.close(fd)
                os.chmod(temp_path, 0o644)
                # Set ownership to asterisk user if running as root
                try:
                    import pwd
                    pw = pwd.getpwnam("asterisk")
                    os.chown(temp_path, pw.pw_uid, pw.pw_gid)
                except (KeyError, PermissionError):
                    pass
                os.rename(temp_path, path)
                written.append(path)
                log.info("Wrote %s (%d bytes)", path, len(content))
            except Exception:
                os.close(fd) if not os.get_inheritable(fd) else None
                if os.path.exists(temp_path):
                    os.unlink(temp_path)
                raise
        return written

    def write_and_reload(self, ami_command_fn) -> dict:
        """Write configs to disk and reload Asterisk via AMI.

        Args:
            ami_command_fn: function that sends AMI command dict → response string

        Returns:
            {"files_written": [...], "reload_responses": [...]}
        """
        written = self.write_configs()

        reload_responses = []
        try:
            resp = ami_command_fn({"Action": "Command", "Command": "dialplan reload"})
            reload_responses.append(f"dialplan: {resp.strip()[:200]}")
        except Exception as exc:
            reload_responses.append(f"dialplan reload failed: {exc}")

        try:
            resp = ami_command_fn({"Action": "Command", "Command": "module reload res_pjsip.so"})
            reload_responses.append(f"pjsip: {resp.strip()[:200]}")
        except Exception as exc:
            reload_responses.append(f"pjsip reload failed: {exc}")

        return {
            "files_written": written,
            "reload_responses": reload_responses,
        }


