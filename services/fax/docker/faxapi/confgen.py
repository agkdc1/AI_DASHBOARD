    def _gen_voicemail(self) -> str:
        """Generate voicemail_generated.conf."""
        conn = self._conn()
        extensions = conn.execute(
            "SELECT ext, name FROM extensions WHERE mailbox = 1 ORDER BY ext"
        ).fetchall()
        conn.close()

        lines = [
            f"; voicemail_generated.conf — Auto-generated by confgen.py",
            f"; Generated: {datetime.now().isoformat()}",
            "",
            "[device]",
        ]

        for ext in extensions:
            # Format: ext => password,name,email,pager,options
            lines.append(f"{ext['ext']} => ,{ext['name']},,,"
                         f"attach=yes|envelope=yes|delete=no|saycid=yes")

        return "\n".join(lines) + "\n"

