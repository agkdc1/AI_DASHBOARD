"""Asterisk config generator — reads SQLite PBX database, writes .conf files.

Generates:
  - pjsip_extensions.conf  (endpoint, auth, AOR for each extension)
  - extensions_generated.conf  (dialplan: routing, ring groups, day/night, feature codes, IVR)
  - voicemail_generated.conf  (voicemail boxes)

Preserved files (NOT generated, kept as-is):
  - pjsip_ntt_dynamic.conf  (NTT trunk — rendered by vault/faxapi DHCP handler)
  - extensions_custom.conf   (hand-written NTT/fax contexts)
  - pjsip.conf / pjsip.transports.conf  (transport config)
"""

import logging
import os
import sqlite3
import tempfile
from datetime import datetime
from pathlib import Path

log = logging.getLogger(__name__)

# Default paths — overridden in container environment
DEFAULT_DB_PATH = "/var/lib/asterisk/pbx.db"
DEFAULT_ASTERISK_DIR = "/etc/asterisk"


class ConfGen:
    """Generate Asterisk config files from SQLite PBX database."""

    def __init__(self, db_path: str = DEFAULT_DB_PATH, asterisk_dir: str = DEFAULT_ASTERISK_DIR):
        self.db_path = db_path
        self.asterisk_dir = asterisk_dir

    def _conn(self) -> sqlite3.Connection:
        conn = sqlite3.connect(self.db_path)
        conn.row_factory = sqlite3.Row
        return conn

    # ------------------------------------------------------------------
    # PJSIP extensions config
    # ------------------------------------------------------------------

    def _gen_pjsip_extensions(self) -> str:
        """Generate pjsip_extensions.conf with endpoint/auth/aor for each extension."""
        conn = self._conn()
        extensions = conn.execute("SELECT * FROM extensions ORDER BY ext").fetchall()
        conn.close()

        lines = [
            f"; pjsip_extensions.conf — Auto-generated by confgen.py",
            f"; Generated: {datetime.now().isoformat()}",
            f"; Extensions: {len(extensions)}",
            f"; DO NOT EDIT — changes will be overwritten on next confgen run.",
            "",
        ]

        for ext in extensions:
            e = ext["ext"]
            name = ext["name"]
            pw = ext["password"]
            ctx = ext["context"]

            # Endpoint
            lines.append(f"[{e}]")
            lines.append("type=endpoint")
            lines.append("transport=0.0.0.0-udp")
            lines.append(f"aors={e}")
            lines.append(f"auth={e}-auth")
            lines.append("tos_audio=ef")
            lines.append("cos_audio=5")
            lines.append("allow=ulaw")
            lines.append(f"context={ctx}")
            lines.append(f'callerid={name} <{e}>')
            lines.append("dtmf_mode=rfc4733")
            lines.append("direct_media=yes")
            if ext["mailbox"]:
                lines.append(f"mailboxes={e}@device")
            lines.append("trust_id_inbound=yes")
            lines.append("send_pai=yes")
            lines.append("send_connected_line=yes")
            lines.append("rtp_symmetric=yes")
            lines.append("rewrite_contact=yes")
            lines.append("force_rport=yes")
            lines.append("language=ja")
            # Call/pickup groups for *8 (group pickup) and ** (directed pickup)
            call_grp = ext["call_group"] if ext["call_group"] else "1"
            pickup_grp = ext["pickup_group"] if ext["pickup_group"] else "1"
            lines.append(f"named_call_group={call_grp}")
            lines.append(f"named_pickup_group={pickup_grp}")
            if ext["recording"]:
                lines.append("one_touch_recording=on")
                lines.append("record_on_feature=apprecord")
                lines.append("record_off_feature=apprecord")
            lines.append("timers=yes")
            lines.append("timers_min_se=90")
            lines.append("rtp_timeout=30")
            lines.append("rtp_timeout_hold=300")
            lines.append("rtp_keepalive=0")
            lines.append("media_encryption=no")
            lines.append("")

            # Auth
            lines.append(f"[{e}-auth]")
            lines.append("type=auth")
            lines.append("auth_type=userpass")
            lines.append(f"password={pw}")
            lines.append(f"username={e}")
            lines.append("")

            # AOR
            lines.append(f"[{e}]")
            lines.append("type=aor")
            lines.append("max_contacts=1")
            lines.append("remove_existing=yes")
            lines.append("maximum_expiration=7200")
            lines.append("minimum_expiration=60")
            lines.append("qualify_frequency=60")
            if ext["mailbox"]:
                lines.append(f"mailboxes={e}@device")
            lines.append("")

        return "\n".join(lines) + "\n"

    # ------------------------------------------------------------------
    # Dialplan
    # ------------------------------------------------------------------

    def _gen_dialplan(self) -> str:
        """Generate extensions_generated.conf with all dialplan contexts."""
        conn = self._conn()

        lines = [
            f"; extensions_generated.conf — Auto-generated by confgen.py",
            f"; Generated: {datetime.now().isoformat()}",
            f"; DO NOT EDIT — changes will be overwritten on next confgen run.",
            "",
        ]

        # Globals
        lines.extend(self._gen_globals(conn))
        lines.append("")

        # from-internal: extension-to-extension dialing
        lines.extend(self._gen_from_internal(conn))
        lines.append("")

        # from-pstn: inbound DID routing
        lines.extend(self._gen_from_pstn(conn))
        lines.append("")

        # Outbound routes
        lines.extend(self._gen_outbound_routes(conn))
        lines.append("")

        # Ring groups
        lines.extend(self._gen_ring_groups(conn))
        lines.append("")

        # Day/night modes
        lines.extend(self._gen_day_night(conn))
        lines.append("")

        # IVR menus
        lines.extend(self._gen_ivr(conn))
        lines.append("")

        # Feature codes
        lines.extend(self._gen_feature_codes(conn))
        lines.append("")

        # Utility contexts (blackhole, recorded-call, etc.)
        lines.extend(self._gen_utility_contexts(conn))
        lines.append("")

        conn.close()
        return "\n".join(lines) + "\n"

    def _gen_globals(self, conn: sqlite3.Connection) -> list[str]:
        lines = [
            "[globals]",
            "; Trunk config",
            "TRUNK=PJSIP",
            "TRUNK_PEER=ntt-trunk",
            "OUTCID=0312345678",
            "DIAL_OPTIONS=HhTtr",
            "TRUNK_OPTIONS=T",
            "RINGTIMER=15",
            "TRANSFER_CONTEXT=from-internal-xfer",
        ]
        return lines

    def _gen_from_internal(self, conn: sqlite3.Connection) -> list[str]:
        """Internal extension dialing + feature code routing."""
        extensions = conn.execute("SELECT ext, name FROM extensions ORDER BY ext").fetchall()
        feature_codes = conn.execute("SELECT code, context FROM feature_codes WHERE enabled = 1").fetchall()

        lines = [
            "; === Internal dialing ===",
            "[from-internal]",
            "include => from-internal-custom",
            "include => ext-local",
            "include => outbound-allroutes",
            "include => ext-featurecodes",
            "include => ext-group",
            "include => app-pickup",
            "",
            "[from-internal-custom]",
            "; Placeholder for custom internal additions",
            "",
            "; --- Extension routing ---",
            "[ext-local]",
        ]

        for ext in extensions:
            e = ext["ext"]
            lines.append(f"exten => {e},1,NoOp(Dialing extension {e}: {ext['name']})")
            # Set PICKUPMARK for directed pickup (**XXX) and CALLTRACE for *32
            lines.append(f" same => n,Set(__PICKUPMARK=${{EXTEN}})")
            lines.append(f" same => n,Set(DB(CALLTRACE/${{CALLERID(num)}})=${{EXTEN}})")
            lines.append(f" same => n,Set(__RINGTIMER=${{DB(RINGTIMER/{e})}})")
            lines.append(f" same => n,GotoIf(${{LEN(${{RINGTIMER}})}}?:skip_rt)")
            lines.append(f" same => n,Set(DIAL_TIMER=${{RINGTIMER}})")
            lines.append(f" same => n(skip_rt),Set(DIAL_TIMER=${{IF(${{LEN(${{DIAL_TIMER}})}}?${{DIAL_TIMER}}:${{RINGTIMER}})}})")
            # Check call forward unconditional
            lines.append(f" same => n,Set(CFU=${{DB(CFU/{e})}})")
            lines.append(f" same => n,GotoIf(${{LEN(${{CFU}})}}?cf_redirect)")
            # Check call forward busy (set for later)
            lines.append(f" same => n,Set(__CFB=${{DB(CFB/{e})}})")
            lines.append(f" same => n,Set(__CFNA=${{DB(CFNA/{e})}})")
            # Dial the extension
            lines.append(f" same => n,Dial(PJSIP/{e},${{IF(${{LEN(${{DIAL_TIMER}})}}?${{DIAL_TIMER}}:15)}},Ttro(ntt-headers^s^1))")
            # Handle NOANSWER
            lines.append(f" same => n,GotoIf(${{LEN(${{CFNA}})}}?cf_noanswer)")
            lines.append(f" same => n,VoiceMail({e}@device,u)")
            lines.append(f" same => n,Hangup()")
            # Call forward redirect
            lines.append(f" same => n(cf_redirect),Goto(from-internal,${{CFU}},1)")
            lines.append(f" same => n(cf_noanswer),Goto(from-internal,${{CFNA}},1)")
            # Clear PICKUPMARK after call ends
            lines.append(f" same => n,Set(__PICKUPMARK=)")
            lines.append("")

        return lines

    def _gen_from_pstn(self, conn: sqlite3.Connection) -> list[str]:
        """Inbound DID routing (from-pstn is included by extensions.conf)."""
        routes = conn.execute("SELECT * FROM inbound_routes ORDER BY id").fetchall()

        lines = [
            "; === Inbound DID routing (from-pstn) ===",
            "; Note: [from-pstn] is defined in extensions.conf, which includes ext-did.",
            "; [from-ntt] in extensions_custom.conf parses NTT To header → from-pstn.",
            "",
            "[ext-did]",
        ]

        for route in routes:
            did = route["did"]
            dest = route["destination"]
            desc = route["description"] or ""

            if did == "default":
                # Catch-all: match any DID not explicitly routed
                lines.append(f"; Default route: {desc}")
                lines.append(f"exten => _X!,1,NoOp(Inbound DID ${{EXTEN}} — default route)")
                lines.append(f" same => n,Gosub(app-blacklist-check,s,1())")
                lines.append(f" same => n,GotoIf(${{BLACKLISTED}}?app-blackhole,hangup,1)")
                lines.append(f" same => n,Set(CALLERID(name-pres)=allowed)")
                lines.append(f" same => n,Goto({self._dest_to_context(dest)})")
                lines.append("")
                lines.append(f"exten => s,1,NoOp(Inbound DID (no exten) — default route)")
                lines.append(f" same => n,Gosub(app-blacklist-check,s,1())")
                lines.append(f" same => n,GotoIf(${{BLACKLISTED}}?app-blackhole,hangup,1)")
                lines.append(f" same => n,Goto({self._dest_to_context(dest)})")
                lines.append("")
            elif did == "0312345679":
                # Fax line — skip, handled by extensions_custom.conf [from-ntt]
                lines.append(f"; Fax DID {did} — handled by [from-ntt] in extensions_custom.conf")
                lines.append("")
            else:
                lines.append(f"; DID route: {did} → {dest} ({desc})")
                lines.append(f"exten => {did},1,NoOp(Inbound DID {did})")
                lines.append(f" same => n,Gosub(app-blacklist-check,s,1())")
                lines.append(f" same => n,GotoIf(${{BLACKLISTED}}?app-blackhole,hangup,1)")
                lines.append(f" same => n,Goto({self._dest_to_context(dest)})")
                lines.append("")

        return lines

    def _gen_outbound_routes(self, conn: sqlite3.Connection) -> list[str]:
        """Outbound route contexts — pattern matching for trunk routing."""
        routes = conn.execute("SELECT * FROM outbound_routes ORDER BY priority").fetchall()

        lines = [
            "; === Outbound routes ===",
            "[outbound-allroutes]",
        ]

        # Include each route context
        route_names = []
        for route in routes:
            ctx_name = f"outrt-{route['id']}"
            lines.append(f"include => {ctx_name}")
            route_names.append(ctx_name)
        lines.append("")

        for route in routes:
            ctx_name = f"outrt-{route['id']}"
            lines.append(f"; Route: {route['name']} (priority {route['priority']})")
            lines.append(f"[{ctx_name}]")

            pattern = route["pattern"]
            if route["action"] == "BLOCK":
                lines.append(f"exten => {pattern},1,NoOp(BLOCKED: {route['name']})")
                lines.append(f" same => n,Playback(ss-noservice)")
                lines.append(f" same => n,Congestion(20)")
                lines.append(f" same => n,Hangup()")
            else:
                # Strip leading 9 (external access code) for trunk dialing
                lines.append(f"exten => {pattern},1,NoOp(Outbound: {route['name']} via {route['trunk']})")
                pw = route["password"]
                if pw:
                    lines.append(f" same => n,Authenticate({pw})")
                lines.append(f" same => n,Set(OUTNUM=${{EXTEN:1}})")
                lines.append(f" same => n,Set(CALLERID(num)=0312345678)")
                lines.append(f" same => n,Dial(PJSIP/${{OUTNUM}}@{route['trunk']},300,Tb(ntt-headers^s^1))")
                lines.append(f" same => n,Hangup()")
            lines.append("")

        return lines

    def _gen_ring_groups(self, conn: sqlite3.Connection) -> list[str]:
        """Ring group contexts."""
        groups = conn.execute("SELECT * FROM ring_groups ORDER BY id").fetchall()

        lines = [
            "; === Ring groups ===",
            "[ext-group]",
        ]

        for group in groups:
            members = conn.execute(
                "SELECT ext FROM ring_group_members WHERE group_id = ? ORDER BY ext",
                (group["id"],),
            ).fetchall()
            member_list = [m["ext"] for m in members]

            num = group["number"]
            strategy = group["strategy"]
            timeout = group["timeout"]
            dest = group["destination"]

            lines.append(f"; Ring group {num}: {group['description']} ({strategy}, {timeout}s)")
            lines.append(f"exten => {num},1,NoOp(Ring group {num} — {strategy})")

            if strategy == "ringall":
                # Build PJSIP dial string: PJSIP/001&PJSIP/002&...
                dial_str = "&".join(f"PJSIP/{ext}" for ext in member_list)
                lines.append(f" same => n,Dial({dial_str},{timeout},Ttrg)")
            else:
                # Hunt / sequential: dial one by one
                for ext in member_list:
                    lines.append(f" same => n,Dial(PJSIP/{ext},{timeout // len(member_list)},Ttr)")

            # Fallback
            if dest == "hangup":
                lines.append(f" same => n,Hangup()")
            elif dest.startswith("ivr-"):
                ivr_id = dest.split("-", 1)[1]
                lines.append(f" same => n,Goto(ivr-{ivr_id},s,1)")
            else:
                lines.append(f" same => n,Goto({self._dest_to_context(dest)})")
            lines.append("")

        return lines

    def _gen_day_night(self, conn: sqlite3.Connection) -> list[str]:
        """Day/night mode contexts with DB-driven toggle."""
        modes = conn.execute("SELECT * FROM day_night_modes ORDER BY id").fetchall()

        lines = [
            "; === Day/Night modes ===",
            "[app-daynight]",
        ]

        for mode in modes:
            mid = mode["id"]
            day_dest = mode["day_destination"]
            night_dest = mode["night_destination"]
            # FreePBX uses "C" prefix for AstDB keys: DAYNIGHT/C0, DAYNIGHT/C1, etc.
            db_key = f"C{mid}"

            lines.append(f"; Mode {mid}: {mode['name']}")
            lines.append(f"exten => {mid},1,NoOp(Day/Night mode {mid}: {mode['name']})")
            lines.append(f" same => n,Set(DAYNIGHT=${{DB(DAYNIGHT/{db_key})}})")
            lines.append(f" same => n,GotoIf($[${{LEN(${{DAYNIGHT}})}} = 0]?day)")
            lines.append(f" same => n,GotoIf($[${{DAYNIGHT}} = NIGHT]?night:day)")
            lines.append(f" same => n(day),Goto({self._dest_to_context(day_dest)})")
            lines.append(f" same => n(night),Goto({self._dest_to_context(night_dest)})")
            lines.append("")

        # Toggle context
        lines.append("; Day/Night toggle (feature codes *280, *281, *282)")
        lines.append("[app-daynight-toggle]")
        for mode in modes:
            mid = mode["id"]
            pw = mode["password"]
            db_key = f"C{mid}"
            lines.append(f"exten => *28{mid},1,NoOp(Toggle day/night mode {mid})")
            if pw:
                lines.append(f" same => n,Authenticate({pw})")
            lines.append(f" same => n,Set(CURRENT=${{DB(DAYNIGHT/{db_key})}})")
            lines.append(f" same => n,GotoIf($[${{CURRENT}} = NIGHT]?set_day)")
            lines.append(f" same => n,Set(DB(DAYNIGHT/{db_key})=NIGHT)")
            lines.append(f" same => n,Playback(beep)")
            lines.append(f" same => n,Hangup()")
            lines.append(f" same => n(set_day),Set(DB(DAYNIGHT/{db_key})=DAY)")
            lines.append(f" same => n,Playback(beep)")
            lines.append(f" same => n,Hangup()")
            lines.append("")

        return lines

    def _gen_ivr(self, conn: sqlite3.Connection) -> list[str]:
        """IVR menu contexts."""
        menus = conn.execute("SELECT * FROM ivr_menus ORDER BY id").fetchall()

        lines = ["; === IVR Menus ==="]

        for menu in menus:
            mid = menu["id"]
            entries = conn.execute(
                "SELECT digit, destination FROM ivr_entries WHERE ivr_id = ? ORDER BY digit",
                (mid,),
            ).fetchall()

            lines.append(f"; IVR {mid}: {menu['name']} — {menu['description']}")
            lines.append(f"[ivr-{mid}]")
            lines.append(f"exten => s,1,NoOp(IVR {mid}: {menu['name']})")
            lines.append(f" same => n,Answer()")
            lines.append(f" same => n(start),Set(TIMEOUT(response)={menu['timeout']})")
            # Play IVR greeting — use audio_file from DB, fallback to custom/ivr-{id}
            audio = menu["audio_file"] if menu["audio_file"] else f"custom/ivr-{mid}"
            lines.append(f" same => n,Background({audio})")
            lines.append(f" same => n,WaitExten({menu['timeout']})")
            lines.append("")

            # Digit entries
            for entry in entries:
                dest = self._dest_to_context(entry["destination"])
                lines.append(f"exten => {entry['digit']},1,Goto({dest})")
            lines.append("")

            # Timeout
            timeout_dest = menu["timeout_destination"]
            if timeout_dest:
                lines.append(f"exten => t,1,Goto({self._dest_to_context(timeout_dest)})")
            else:
                lines.append(f"exten => t,1,Goto(s,start)")
            lines.append("")

            # Invalid
            invalid_dest = menu["invalid_destination"]
            if invalid_dest:
                lines.append(f"exten => i,1,Goto({self._dest_to_context(invalid_dest)})")
            else:
                lines.append(f"exten => i,1,Goto(s,start)")
            lines.append("")

        return lines

    def _gen_feature_codes(self, conn: sqlite3.Connection) -> list[str]:
        """Feature code contexts."""
        codes = conn.execute("SELECT * FROM feature_codes WHERE enabled = 1 ORDER BY code").fetchall()

        lines = [
            "; === Feature codes ===",
            "[ext-featurecodes]",
        ]

        for fc in codes:
            code = fc["code"]
            ctx = fc["context"]
            lines.append(f"exten => {code},1,Goto({ctx},{code},1)")

        lines.append("")

        # Voicemail
        lines.extend(self._gen_vmail_contexts())
        lines.append("")

        # Call forward on/off
        lines.extend(self._gen_cf_contexts())
        lines.append("")

        # Blacklist
        lines.extend(self._gen_blacklist_contexts())
        lines.append("")

        # Pickup
        lines.extend(self._gen_pickup_contexts(conn))
        lines.append("")

        # Call waiting
        lines.extend(self._gen_cw_contexts())
        lines.append("")

        return lines

